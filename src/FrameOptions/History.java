    public History(String user) {
        initComponents();
        this.setLocationRelativeTo(null);
        welcomeLabel.setText("Hi, " + user);
        Image icon = Toolkit.getDefaultToolkit().getImage("DK1.png");
        this.setIconImage(icon);
        
        int iduser = 0;
        try {
            //Connecting to the database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/EquationsCalculator", "root", "root");

            String checkUser = "SELECT iduser FROM users WHERE username=?";
            PreparedStatement pst = con.prepareStatement(checkUser);

            pst.setString(1, welcomeLabel.getText().substring(4).trim());

            ResultSet rsCheck = pst.executeQuery();
            //Getting the user ID to identify them and display the correct info
            if (rsCheck.next()) {
                iduser = rsCheck.getInt("iduser");

            }
            /**
             * This query will collect the information to be displayed in our
             * list.
             */
            String results = "SELECT equation1 as 'Equation 1', equation2 as 'Equation 2', "
                    + "equation3 as 'Equation 3', results as Result from calculator where "
                    + "iduser=? order by equation3 desc";

            pst = con.prepareStatement(results);
            pst.setInt(1, iduser);

            ResultSet rs = pst.executeQuery();
            String query = String.valueOf(rs.getStatement()).substring(43);
            /**
             * Using FillTable to create this table and display the list of
             * users.
             */
            Utilities.FillTable(tableOfUserResults, query);
            setColumnsWidth(this.tableOfUserResults, 480, 18, 18, 18, 45);
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Something went wrong!", JOptionPane.ERROR_MESSAGE);
        }
        
    }

